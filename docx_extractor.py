# ============================================================
# 1) Caption-type detection (REGEX, CASE-INSENSITIVE)
#    - Matches ALL map types at start of caption
#    - Enforces band ("on 2.4/5 GHz band") only for band-required types
# ============================================================

# Types that Ekahau captions often have WITHOUT "on 2.4/5 GHz band"
NO_BAND_TYPES = {
    "survey routes and access points",
    "associated access point",
    "bluetooth coverage",
}

# Band detector (tolerant of "2.4GHz" or "2.4 GHz", any case)
BAND_RE = re.compile(r"\bon\s+(?:the\s+)?(2\.4|5)\s*ghz\s+band\b", re.IGNORECASE)

# One regex that matches all your VIZ_TYPES at the START of the caption
# (case-insensitive, tolerant of hyphens/spaces in SNR)
TYPE_START_RE = re.compile(r"""
^(?P<type>
    #survey\s*routes\s*and\s*access\s*points|
    signal\s*strength|
    #secondary\s*signal\s*strength|
    #tertiary\s*signal\s*strength|
    (?:signal\s*(?:-|/)?\s*to\s*(?:-|/)?\s*noise\s*ratio)(?:\s*\(snr\))?|
    snr|
    noise|
    data\s*rate|
    throughput|
    channel\s*utilization|
    #channel\s*interference|
    #channel\s*width|
    spectrum\s*channel\s*power|
    #number\s*of\s*aps|
    #network\s*health|
    #network\s*issues|
    #interferers|
    #associated\s*access\s*point|
    #bluetooth\s*coverage
)\b
""", re.IGNORECASE | re.VERBOSE)


def norm_text(t: str) -> str:
    t = (t or "").replace("\u00A0", " ").replace("\n", " ").strip()
    t = re.sub(r"\s+", " ", t)
    if t.endswith("."):
        t = t[:-1]
    return t


def canonical_type(type_text: str) -> str:
    """Normalize the matched type into a stable lowercase key."""
    s = (type_text or "").lower()
    s = s.replace("–", "-").replace("—", "-")
    s = re.sub(r"\s+", " ", s).strip()

    # Collapse SNR variants into a single key
    if s == "snr" or ("noise ratio" in s and "signal" in s):
        return "snr"

    return s


def is_caption_text(s: str) -> bool:
    """
    Generic caption matcher:
    - Allows optional 'Figure 12:' prefix
    - Requires a supported type at the start (TYPE_START_RE)
    - Requires the word 'for' somewhere (reduces false matches on headings)
    - Enforces band only when type is NOT in NO_BAND_TYPES
    """
    s = norm_text(s)
    if not s:
        return False

    # remove "Figure 12:" / "Fig 12 - " prefix
    s2 = re.sub(r"^(figure|fig)\s*\d+\s*[:\-]\s*", "", s, flags=re.IGNORECASE).strip()

    m = TYPE_START_RE.match(s2)
    if not m:
        return False

    # Typical Ekahau captions include "for <location ...>"
    if re.search(r"\bfor\b", s2, re.IGNORECASE) is None:
        return False

    t_key = canonical_type(m.group("type"))

    # If this caption type normally has a band, enforce it
    if t_key not in NO_BAND_TYPES:
        if BAND_RE.search(s2) is None:
            return False

    return True


