# 6) Extract maps for each caption (caption can be above OR below)
#    - saves 1 image too (scale naming if scale-like)
# ============================================================
def extract_maps_best_side_save_one(docx_path: str, out_dir: str, MIN_ICON_AREA: int = 250):
    os.makedirs(out_dir, exist_ok=True)

    device = device_from_docx(docx_path)
    doc = Document(docx_path)

    blocks = []
    for p in iter_all_paragraphs(doc):
        t = norm_text(p.text)
        blocks.append({
            "text": t,
            "is_caption": is_caption_text(t),
            "rids": list(paragraph_image_rids(p))
        })

    cap_idxs = [i for i, b in enumerate(blocks) if b["is_caption"]]

    def resolve_imgs(rids):
        rids = list(dict.fromkeys(rids))
        imgs = []
        for rid in rids:
            part = doc.part.related_parts.get(rid)
            if part is None:
                continue
            blob = part.blob
            w, h = get_dims(blob)
            area = (w * h) if (w and h) else 0

            if area and area < MIN_ICON_AREA:
                continue

            imgs.append({
                "blob": blob,
                "ext": ext_from_part(part),
                "w": w, "h": h,
                "is_scale": looks_like_scale(w, h),
                "score": score(blob),
            })
        return imgs

    def pick_main_scale(imgs):
        imgs_sorted = sorted(imgs, key=lambda x: x["score"], reverse=True)
        main = imgs_sorted[0]
        rest = imgs_sorted[1:]
        scale_candidates = [x for x in rest if x["is_scale"]]
        if scale_candidates:
            scale = sorted(scale_candidates, key=lambda x: x["score"])[0]
        else:
            scale = sorted(rest, key=lambda x: x["score"])[0]
        return main, scale

    def side_quality(imgs):
        n = len(imgs)
        if n == 0:
            return -10**9
        has_scale_like = any(x["is_scale"] for x in imgs)
        q = 0
        q += 200 if n == 2 else 0
        q -= 30 * abs(n - 2)
        q += 40 if has_scale_like else 0
        return q

    saved = []
    skipped0 = 0

    for pos, i in enumerate(cap_idxs):
        caption = blocks[i]["text"]
        cap_safe = safe_filename(caption)

        prev_i = cap_idxs[pos - 1] if pos > 0 else None
        next_i = cap_idxs[pos + 1] if (pos + 1 < len(cap_idxs)) else len(blocks)

        # AFTER caption region
        rids_after = []
        for b in blocks[i+1:next_i]:
            rids_after.extend(b["rids"])
        imgs_after = resolve_imgs(rids_after)

        # BEFORE caption region
        rids_before = []
        start = (prev_i + 1) if prev_i is not None else 0
        for b in blocks[start:i]:
            rids_before.extend(b["rids"])
        imgs_before = resolve_imgs(rids_before)

        imgs = imgs_after if side_quality(imgs_after) >= side_quality(imgs_before) else imgs_before

        if len(imgs) == 0:
            skipped0 += 1
            continue

        # save single image
        if len(imgs) == 1:
            one = imgs[0]
            suffix = "_scale" if one["is_scale"] else ""
            outp = unique_path(os.path.join(out_dir, f"{device}_{cap_safe}{suffix}.{one['ext']}"))
            with open(outp, "wb") as f:
                f.write(one["blob"])
            saved.append(outp)
            continue

        # save main+scale
        main, scale = pick_main_scale(imgs)
        main_path  = unique_path(os.path.join(out_dir, f"{device}_{cap_safe}.{main['ext']}"))
        scale_path = unique_path(os.path.join(out_dir, f"{device}_{cap_safe}_scale.{scale['ext']}"))

        with open(main_path, "wb") as f:
            f.write(main["blob"])
        with open(scale_path, "wb") as f:
            f.write(scale["blob"])

        saved.extend([main_path, scale_path])

    return {
        "saved_paths": saved,
        "matched_captions": len(cap_idxs),
        "skipped_zero_image": skipped0,
        "device": device,
    }


