"""## Build chained similarity transforms (floor → base) in meters"""

# =========================
# 9) Build similarity transforms (floor -> global(base)) in METERS
#    p_global = s*R*p_local + t
# =========================
def px_to_m(floor_name, xy_px):
    mpu = float(FLOORS_META[floor_name]["metersPerUnit"])
    return np.array([xy_px[0]*mpu, xy_px[1]*mpu], dtype=float)

def build_similarity_from_2pts(A1, A2, B1, B2):
    # A*,B* are 2D points in meters
    A1 = np.array(A1, float); A2=np.array(A2,float)
    B1 = np.array(B1, float); B2=np.array(B2,float)
    vA = A2 - A1
    vB = B2 - B1
    nA = np.linalg.norm(vA)
    nB = np.linalg.norm(vB)
    if nA < 1e-9 or nB < 1e-9:
        raise ValueError("Anchors too close; pick two distinct points.")
    s = nB / nA
    angA = np.arctan2(vA[1], vA[0])
    angB = np.arctan2(vB[1], vB[0])
    th = angB - angA
    c, si = np.cos(th), np.sin(th)
    R = np.array([[c, -si],[si, c]], float)
    t = B1 - (s * (R @ A1))
    return s, R, t

def apply_sim(p, s, R, t):
    p = np.array(p, float)
    return s*(R@p) + t

def invert_sim(p, s, R, t):
    p = np.array(p, float)
    return (R.T @ ((p - t)/s))

