def _prepend_zero_point(x, y, method='linear', clip_0_100=True, clamp_to_minmax=False):
    x = np.asarray(x, float)
    y = np.asarray(y, float)
    if len(x) == 0:
        return x, y
    order = np.argsort(x)
    x = x[order]
    y = y[order]
    if x[0] <= 1e-9:
        if clip_0_100:
            y = np.clip(y, 0, 100)
        return x, y
    if method == 'linear' and len(x) >= 2 and (x[1] - x[0]) != 0:
        y0 = y[0] + (0.0 - x[0]) * (y[1] - y[0]) / (x[1] - x[0])
    else:
        y0 = y[0]
    if clamp_to_minmax:
        y0 = float(np.clip(y0, float(np.nanmin(y)), float(np.nanmax(y))))
    x2 = np.concatenate([[0.0], x])
    y2 = np.concatenate([[y0], y])
    if clip_0_100:
        y2 = np.clip(y2, 0, 100)
    return x2, y2

def compute_avg_drop_per_ft_from_xy(x, y) -> float:
    x = np.asarray(x, float)
    y = np.asarray(y, float)
    if len(x) < 2:
        return np.nan
    order = np.argsort(x)
    x = x[order]
    y = y[order]
    dx = np.diff(x)
    dy = np.diff(y)
    m = (dx > 1e-9) & np.isfinite(dx) & np.isfinite(dy)
    if not np.any(m):
        return np.nan
    # drop per ft (positive when y decreases)
    return float(np.nanmean(-dy[m] / dx[m]))

