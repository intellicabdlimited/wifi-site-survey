def _infer_router_key_from_filename(path: str) -> str:
    b = os.path.basename(path)
    # common pattern: "TMO-G5AR February 2026 Survey (No APs).esx"
    m = re.match(r"^([A-Za-z0-9_-]+)\s+", b)
    if m:
        return m.group(1)
    return os.path.splitext(b)[0]

def _read_esx_json(esx_path: str, member: str):
    with zipfile.ZipFile(esx_path, "r") as z:
        return json.loads(z.read(member).decode("utf-8"))

def _find_ap_placement_files(esx_path: str):
    with zipfile.ZipFile(esx_path, "r") as z:
        names = z.namelist()
    patt = re.compile(r"access.*(placement|location|position).*\.json$", re.IGNORECASE)
    return [n for n in names if patt.search(n)]

def _extract_xy_floor_from_obj(obj):
    """
    Recursively find dicts containing numeric x,y and (optional) floorPlanId.
    Returns list of (x,y,floorPlanId_or_None).
    """
    out = []
    def rec(o, inherited_floor=None):
        if isinstance(o, dict):
            floor = o.get("floorPlanId") or o.get("floorplanId") or o.get("floor_id") or inherited_floor
            if ("x" in o) and ("y" in o) and isinstance(o["x"], (int, float)) and isinstance(o["y"], (int, float)):
                out.append((float(o["x"]), float(o["y"]), floor))
            for _, v in o.items():
                rec(v, floor)
        elif isinstance(o, list):
            for v in o:
                rec(v, inherited_floor)
    rec(obj, None)
    return out

def extract_mine_ap_candidates(esx_path: str, base_floor_name: str):
    """
    Returns candidates list:
      [{ap_id, ap_name, floorPlanId, floorPlanName, x, y, metersPerUnit}]
    Only includes APs with mine:true and WITH coordinates (if present).
    """
    try:
        fp = _read_esx_json(esx_path, "floorPlans.json")["floorPlans"]
        fp_by_id = {f["id"]: f for f in fp}
    except Exception as e:
        print("[WARN] floorPlans.json missing/invalid in", os.path.basename(esx_path), ":", e)
        fp_by_id = {}

    try:
        aps = _read_esx_json(esx_path, "accessPoints.json")["accessPoints"]
    except Exception as e:
        print("[WARN] accessPoints.json missing/invalid in", os.path.basename(esx_path), ":", e)
        return []

    mine_aps = [ap for ap in aps if ap.get("mine") is True]
    if not mine_aps:
        return []

    placement_files = _find_ap_placement_files(esx_path)

    candidates = []
    for ap in mine_aps:
        ap_id = ap.get("id")
        ap_name = ap.get("name", "AP")

        # 1) try inside AP object
        xys = _extract_xy_floor_from_obj(ap)

        # 2) try external placement files (if any)
        if not xys and placement_files:
            for pf in placement_files:
                try:
                    data = _read_esx_json(esx_path, pf)
                except Exception:
                    continue
                # search for objects that mention this accessPointId
                hits = []
                def rec(o):
                    if isinstance(o, dict):
                        if o.get("accessPointId") == ap_id or o.get("access_point_id") == ap_id:
                            hits.append(o)
                        for v in o.values():
                            rec(v)
                    elif isinstance(o, list):
                        for v in o:
                            rec(v)
                rec(data)
                for h in hits:
                    xys.extend(_extract_xy_floor_from_obj(h))

        # keep only x,y with some floorPlanId found (if possible)
        best = None
        for (x, y, fpid) in xys:
            if fpid is not None:
                best = (x, y, fpid)
                break
        if best is None and xys:
            # no floorPlanId found, keep first
            best = xys[0]

        if best is None:
            continue

        x, y, fpid = best
        fpinfo = fp_by_id.get(fpid, {})
        floor_name = fpinfo.get("name", str(fpid))
        mpu = float(fpinfo.get("metersPerUnit", 1.0))

        candidates.append({
            "ap_id": ap_id,
            "ap_name": ap_name,
            "floorPlanId": fpid,
            "floorPlanName": floor_name,
            "x": float(x),
            "y": float(y),
            "metersPerUnit": mpu
        })

    # Prefer candidates that are on base floor by NAME match
    base_cands = [c for c in candidates if str(c["floorPlanName"]).strip().lower() == str(base_floor_name).strip().lower()]
    return base_cands if base_cands else candidates

def _dist_m(c1, c2):
    # compare in meters using each candidate metersPerUnit
    x1m = c1["x"] * c1.get("metersPerUnit", 1.0)
    y1m = c1["y"] * c1.get("metersPerUnit", 1.0)
    x2m = c2["x"] * c2.get("metersPerUnit", 1.0)
    y2m = c2["y"] * c2.get("metersPerUnit", 1.0)
    return float(np.hypot(x1m - x2m, y1m - y2m))

def choose_consistent_dut_per_router(router_to_candidates: dict, base_floor_name: str, max_iters: int = 8):
    """
    If a router has multiple mine:true candidates, choose the one minimizing
    average distance to other routers' chosen DUTs (on base floor if possible).
    """
    # Flatten all candidates to compute initial centroid (meters)
    all_pts = []
    for rk, cands in router_to_candidates.items():
        for c in cands:
            if str(c["floorPlanName"]).strip().lower() == str(base_floor_name).strip().lower():
                all_pts.append((c["x"]*c["metersPerUnit"], c["y"]*c["metersPerUnit"]))
    if not all_pts:
        # no base-floor candidates anywhere; use all
        for rk, cands in router_to_candidates.items():
            for c in cands:
                all_pts.append((c["x"]*c["metersPerUnit"], c["y"]*c["metersPerUnit"]))

    if all_pts:
        cx = float(np.mean([p[0] for p in all_pts]))
        cy = float(np.mean([p[1] for p in all_pts]))
    else:
        cx, cy = 0.0, 0.0

    # initial selection: closest-to-centroid
    selected = {}
    for rk, cands in router_to_candidates.items():
        if not cands:
            continue
        def score_to_centroid(c):
            xm, ym = c["x"]*c["metersPerUnit"], c["y"]*c["metersPerUnit"]
            return float(np.hypot(xm - cx, ym - cy))
        selected[rk] = min(cands, key=score_to_centroid)

    # iterative refinement: minimize mean distance to others
    for _ in range(max_iters):
        changed = False
        for rk, cands in router_to_candidates.items():
            if not cands:
                continue
            others = [selected[k] for k in selected.keys() if k != rk]
            if not others:
                continue

            def mean_dist(c):
                # only compare with others on same base floor name if possible
                dists = []
                for o in others:
                    if (str(c["floorPlanName"]).strip().lower() == str(base_floor_name).strip().lower() and
                        str(o["floorPlanName"]).strip().lower() == str(base_floor_name).strip().lower()):
                        dists.append(_dist_m(c, o))
                if not dists:
                    # fallback compare to all
                    dists = [_dist_m(c, o) for o in others]
                return float(np.mean(dists)) if dists else 1e18

            best = min(cands, key=mean_dist)
            if best["ap_id"] != selected[rk]["ap_id"]:
                selected[rk] = best
                changed = True
        if not changed:
            break
    return selected

