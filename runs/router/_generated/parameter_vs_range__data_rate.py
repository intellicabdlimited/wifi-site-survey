def to_percent(values: np.ndarray, higher_is_better: bool, mode: str):
    v = values.astype(float)
    v = v[np.isfinite(v)]
    if len(v) == 0:
        return None
    vmin, vmax = float(np.min(v)), float(np.max(v))
    if vmax == vmin:
        return (vmin, vmax, lambda arr: np.full_like(arr, 100.0, dtype=float))
    if mode == 'max' and vmax > 0:
        def f(arr):
            p = 100.0 * (arr / vmax)
            return np.clip(p, 0, 100)
    else:
        def f(arr):
            p = 100.0 * (arr - vmin) / (vmax - vmin)
            return np.clip(p, 0, 100)
    if not higher_is_better:
        return (vmin, vmax, lambda arr: 100.0 - f(arr))
    return (vmin, vmax, f)

