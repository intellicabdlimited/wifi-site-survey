def _prepend_zero_point(x, y, method="linear", clamp_to_data=True):
    """
    Prepend a point at x=0. If method='linear', extrapolation can overshoot.
    clamp_to_data=True clamps y0 to [min(y), max(y)] so it never exceeds CSV-derived range.
    """
    x = np.asarray(x, float)
    y = np.asarray(y, float)
    if len(x) == 0:
        return x, y

    order = np.argsort(x)
    x = x[order]
    y = y[order]

    if x[0] <= 1e-9:
        return x, y

    if method == "linear" and len(x) >= 2 and (x[1] - x[0]) != 0:
        y0 = y[0] + (0.0 - x[0]) * (y[1] - y[0]) / (x[1] - x[0])
    else:
        y0 = y[0]  # flat

    if clamp_to_data:
        finite = np.isfinite(y)
        if np.any(finite):
            ymin = float(np.nanmin(y[finite]))
            ymax = float(np.nanmax(y[finite]))
            y0 = float(np.clip(y0, ymin, ymax))

    return np.concatenate([[0.0], x]), np.concatenate([[y0], y])


def _is_pct_like(name: str) -> bool:
    s = str(name).lower()
    return ("pct" in s) or ("percent" in s) or s.endswith("%")

def pick_actual_y_col(df: pd.DataFrame, y_stat: str) -> str:
    """
    Choose a likely RAW/ACTUAL column (avoid percent columns).
    Priority:
      1) y_stat if present and not pct-like
      2) common raw columns
      3) fallback: first numeric non-pct column
    """
    cols = list(df.columns)
    cols_set = set(cols)
    y_stat = str(y_stat)

    # If y_stat exists and doesn't look like percent, use it
    if y_stat in cols_set and not _is_pct_like(y_stat):
        return y_stat

    # Common raw column names in your pipeline(s)
    for c in ["metric_p50", "p50", "value_p50", "metric", "value", "mean", "metric_mean"]:
        if c in cols_set and not _is_pct_like(c):
            return c

    # If y_stat exists but looks percent, try stripping suffixes
    if y_stat in cols_set and _is_pct_like(y_stat):
        base = re.sub(r"(_pct|_percent|%)+$", "", y_stat, flags=re.IGNORECASE)
        if base in cols_set and not _is_pct_like(base):
            return base

    # fallback: first numeric non-pct column
    for c in cols:
        if _is_pct_like(c):
            continue
        if pd.api.types.is_numeric_dtype(df[c]):
            return c

    # last resort
    return y_stat
