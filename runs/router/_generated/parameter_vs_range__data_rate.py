def sha256_file(path: str) -> str:
    h = hashlib.sha256()
    with open(path, "rb") as f:
        for chunk in iter(lambda: f.read(1024 * 1024), b""):
            h.update(chunk)
    return h.hexdigest()

# ----------------------
# ESX caching (in-memory + on-disk)
# ----------------------
_FILE_HASH_CACHE = {}
_ESX_JSON_MEM = {}

def _file_sha_cached(path: str) -> str:
    """Compute sha256 once per (path,size,mtime)."""
    st = os.stat(path)
    key = (path, st.st_size, int(st.st_mtime))
    if key in _FILE_HASH_CACHE:
        return _FILE_HASH_CACHE[key]
    sha = sha256_file(path)
    # purge older keys for same path
    for k in list(_FILE_HASH_CACHE.keys()):
        if k[0] == path and k != key:
            _FILE_HASH_CACHE.pop(k, None)
    _FILE_HASH_CACHE[key] = sha
    return sha

def _safe_member_name(member: str) -> str:
    return re.sub(r"[^A-Za-z0-9_.-]+", "_", member)

def read_esx_json(esx_path: str, member: str) -> dict:
    """Read JSON member from an .esx (zip). Uses CACHE_DIR if enabled."""
    if not globals().get("CACHE_ENABLED", False):
        with zipfile.ZipFile(esx_path, "r") as zf:
            return json.loads(zf.read(member).decode("utf-8"))

    esx_sha = _file_sha_cached(esx_path)
    mem_key = (esx_sha, member)
    if mem_key in _ESX_JSON_MEM:
        return _ESX_JSON_MEM[mem_key]

    cache_path = os.path.join(CACHE_DIR, f"{CACHE_VERSION}__esxjson__{esx_sha}__{_safe_member_name(member)}.json")
    if os.path.exists(cache_path):
        with open(cache_path, "r", encoding="utf-8") as f:
            obj = json.load(f)
        _ESX_JSON_MEM[mem_key] = obj
        if globals().get("CACHE_VERBOSE", False):
            print(f"[CACHE] hit esx json: {os.path.basename(esx_path)} :: {member}")
        return obj

    with zipfile.ZipFile(esx_path, "r") as zf:
        obj = json.loads(zf.read(member).decode("utf-8"))

    _ESX_JSON_MEM[mem_key] = obj
    try:
        with open(cache_path, "w", encoding="utf-8") as f:
            json.dump(obj, f)
    except Exception as e:
        if globals().get("CACHE_VERBOSE", False):
            print("[CACHE] write failed:", cache_path, e)
    return obj

def extract_floor_images(master_esx: str, out_dir: str) -> dict:
    """
    Extract floor plan images from master ESX using image-{imageId}.
    Returns FLOORS_META: {floorName: {floorPlanId, metersPerUnit, img_path, w,h}}
    Uses CACHE_DIR if enabled.
    """
    os.makedirs(out_dir, exist_ok=True)

    esx_sha = _file_sha_cached(master_esx) if globals().get("CACHE_ENABLED", False) else None
    meta_cache = os.path.join(CACHE_DIR, f"{CACHE_VERSION}__floorsmeta__{esx_sha}.json") if esx_sha else None

    if globals().get("CACHE_ENABLED", False) and meta_cache and os.path.exists(meta_cache):
        try:
            with open(meta_cache, "r", encoding="utf-8") as f:
                cached = json.load(f)
            # Validate image paths exist; if yes, return immediately
            ok = True
            for _, info in cached.items():
                if not os.path.exists(info.get("img_path", "")):
                    ok = False
                    break
            if ok:
                if globals().get("CACHE_VERBOSE", False):
                    print("[CACHE] hit floors meta:", os.path.basename(master_esx))
                return cached
        except Exception:
            pass  # fall through to re-extract

    fps = read_esx_json(master_esx, "floorPlans.json")["floorPlans"]

    floors_meta = {}
    with zipfile.ZipFile(master_esx, "r") as zf:
        names = set(zf.namelist())
        for fp in fps:
            name = str(fp.get("name", "")).strip()
            image_id = fp.get("imageId")
            member = f"image-{image_id}"
            if member not in names:
                continue

            safe = re.sub(r"[^A-Za-z0-9_-]+", "_", name)
            out_path = os.path.join(out_dir, f"{safe}.png")

            if os.path.exists(out_path):
                img = cv2.imread(out_path)
            else:
                img = cv2.imdecode(np.frombuffer(zf.read(member), np.uint8), cv2.IMREAD_COLOR)
                cv2.imwrite(out_path, img)

            h, w = img.shape[:2]
            floors_meta[name] = {
                "floorPlanId": fp.get("id"),
                "metersPerUnit": fp.get("metersPerUnit", 1.0),
                "img_path": out_path,
                "w": int(w),
                "h": int(h),
            }

    if globals().get("CACHE_ENABLED", False) and meta_cache:
        try:
            with open(meta_cache, "w", encoding="utf-8") as f:
                json.dump(floors_meta, f)
        except Exception as e:
            if globals().get("CACHE_VERBOSE", False):
                print("[CACHE] floors meta write failed:", meta_cache, e)

    return floors_meta

