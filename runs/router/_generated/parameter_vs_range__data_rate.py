# Fixed zip name (same every run -> overwrites)
ZIP_NAME = "rvr_full_output.zip"
ZIP_PATH = 'runs/router/rvr_full_output_data_rate.zip'

def zip_folder_overwrite(src_dir: str, zip_path: str):
    src_dir = os.path.abspath(src_dir)
    if not os.path.isdir(src_dir):
        raise FileNotFoundError(f"SRC_DIR not found: {src_dir}")

    # Remove old zip if exists
    if os.path.exists(zip_path):
        os.remove(zip_path)
        print("[OK] Removed previous zip:", zip_path)

    files_added = 0
    with zipfile.ZipFile(zip_path, "w", compression=zipfile.ZIP_DEFLATED) as zf:
        for p in Path(src_dir).rglob("*"):
            if p.is_file():
                arcname = os.path.relpath(str(p), start=os.path.dirname(src_dir))
                zf.write(str(p), arcname=arcname)
                files_added += 1

    size_mb = os.path.getsize(zip_path) / (1024 * 1024)
    print(f"[OK] Created zip: {zip_path}")
    print(f"[INFO] Files added: {files_added}")
    print(f"[INFO] Zip size: {size_mb:.2f} MB")
    return zip_path

zip_path = zip_folder_overwrite(SRC_DIR, ZIP_PATH)

