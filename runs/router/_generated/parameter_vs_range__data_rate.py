def pick_points_local(image_path: str, n_points: int = 1, title: str = "Click points"):
    """Pick N points in a local desktop session (VS Code) using OpenCV.

    - Left-click to add a point.
    - The window closes automatically after N clicks.
    - Press ESC to cancel (raises KeyboardInterrupt).
    """
    img = cv2.imread(image_path)
    if img is None:
        raise FileNotFoundError(image_path)

    pts = []
    disp = img.copy()
    win = f"{title} :: {os.path.basename(image_path)}"
    cv2.namedWindow(win, cv2.WINDOW_NORMAL)

    def _cb(event, x, y, flags, param):
        nonlocal disp
        if event == cv2.EVENT_LBUTTONDOWN:
            pts.append((float(x), float(y)))
            cv2.circle(disp, (x, y), 6, (0, 0, 255), -1)
            cv2.putText(disp, str(len(pts)-1), (x+10, y-10),
                        cv2.FONT_HERSHEY_SIMPLEX, 0.7, (0,0,255), 2)
            cv2.imshow(win, disp)

    cv2.setMouseCallback(win, _cb)
    cv2.imshow(win, disp)

    while True:
        key = cv2.waitKey(20) & 0xFF
        if key == 27:  # ESC
            cv2.destroyWindow(win)
            raise KeyboardInterrupt("Point picking cancelled (ESC).")

        if len(pts) >= int(n_points):
            cv2.destroyWindow(win)
            break

    return np.array(pts, dtype=float)

