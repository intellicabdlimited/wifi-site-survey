def _check_reachable_to_base(site: dict, floors: list, base_floor: str):
    links = site.get("links", {})
    for f in floors:
        if f == base_floor:
            continue
        seen = set()
        cur = f
        while cur != base_floor:
            if cur in seen:
                raise ValueError(f"Cycle detected in links while resolving {f}.")
            seen.add(cur)
            if cur not in links:
                raise ValueError(f"Missing link for floor '{cur}'.")
            cur = links[cur].get("align_to")
            if not cur:
                raise ValueError(f"Link for '{seen}' missing 'align_to'.")
            if cur not in floors:
                raise ValueError(f"Link for '{seen}' points to unknown floor '{cur}'.")
    return True

