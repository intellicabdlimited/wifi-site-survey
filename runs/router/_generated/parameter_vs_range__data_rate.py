# --- Handle chained transforms (recursive) ---
def get_full_transform(site_obj, target_floor, current_floor, current_s=1.0, current_R=np.eye(2), current_t=np.zeros(2)):
    if current_floor == target_floor:
        return {"s": current_s, "R": current_R, "t": current_t}

    link = site_obj["links"].get(current_floor)
    if not link:
        raise ValueError(f"No link found for floor '{current_floor}'. Cannot chain transform.")

    align_to_floor = link["align_to"]

    # Get anchors in meters
    mpu_child = float(FLOORS_META[current_floor]["metersPerUnit"])
    child_stair_m = np.array(link["child_anchors_px"]["stair_px"]) * mpu_child
    child_ref_m = np.array(link["child_anchors_px"]["ref_px"]) * mpu_child

    mpu_parent = float(FLOORS_META[align_to_floor]["metersPerUnit"])
    parent_stair_m = np.array(link["parent_anchors_px"]["stair_px"]) * mpu_parent
    parent_ref_m = np.array(link["parent_anchors_px"]["ref_px"]) * mpu_parent

    # Compute similarity transform from child to parent
    s_child_to_parent, R_child_to_parent, t_child_to_parent = build_similarity_from_2pts(
        child_stair_m, child_ref_m,
        parent_stair_m, parent_ref_m
    )

    # Combine with accumulated transform
    new_s = current_s * s_child_to_parent
    new_R = current_R @ R_child_to_parent
    new_t = current_s * (current_R @ t_child_to_parent) + current_t

    return get_full_transform(site_obj, target_floor, align_to_floor, new_s, new_R, new_t)

