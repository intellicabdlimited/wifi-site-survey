def compute_global_distance_ft(floor_name: str, cx_px: np.ndarray, cy_px: np.ndarray) -> np.ndarray:
    # Convert local floor pixels to global meters, then distance to DUT_GLOBAL_M.
    mpu = float(FLOORS_META[floor_name]["metersPerUnit"])
    pts_local_m = np.stack([cx_px*mpu, cy_px*mpu], axis=1)  # Nx2 in meters
    tf = TRANSFORMS[floor_name]
    pts_global_m = (tf["s"] * (pts_local_m @ tf["R"].T)) + tf["t"]  # Nx2
    dxy = pts_global_m - DUT_GLOBAL_M.reshape(1,2)
    d2 = (dxy[:,0]**2 + dxy[:,1]**2)

    if USE_3D:
        elev = SITE.get("floor_elevation_m", {})
        z = float(elev.get(floor_name, 0.0))
        z0 = float(elev.get(BASE_FLOOR, 0.0))
        d2 = d2 + (z - z0)**2

    return np.sqrt(d2) * FEET_PER_METER

def aggregate_by_distance(df: pd.DataFrame, bin_ft: float) -> pd.DataFrame:
    df = df.replace([np.inf, -np.inf], np.nan).dropna(subset=["distance_ft","value"])
    if df.empty:
        return pd.DataFrame()
    max_d = float(df["distance_ft"].max())
    edges = np.arange(0.0, max_d + bin_ft, bin_ft)
    if len(edges) < 2:
        edges = np.array([0.0, max_d + bin_ft])
    df = df.copy()
    df["bin"] = pd.cut(df["distance_ft"], bins=edges, include_lowest=True, right=False)

    g = df.groupby("bin", observed=True)["value"]
    def q(arr, p):
        return float(np.nanquantile(arr, p)) if len(arr) else np.nan

    mids = []
    for iv in g.groups.keys():
        mids.append(float(iv.left + (iv.right-iv.left)/2.0))

    out = pd.DataFrame({
        "dist_ft_mid": mids,
        "n": g.size().values,
        "mean": g.mean().values,
        "p50": g.median().values,
        "p10": g.apply(lambda s: q(s.values, 0.10)).values,
        "p90": g.apply(lambda s: q(s.values, 0.90)).values,
        "min": g.min().values,
        "max": g.max().values,
    })
    return out.sort_values("dist_ft_mid").reset_index(drop=True)


