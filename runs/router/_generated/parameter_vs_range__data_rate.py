def infer_router_key_from_path(path: str) -> str:
    b = os.path.basename(path)
    # common pattern: "TMO-G5AR February 2026 Survey (No APs).esx"
    m = re.match(r"^([A-Za-z0-9_-]+)\s+", b)
    if m:
        return m.group(1)
    return os.path.splitext(b)[0]

def router_display_name(esx_path: str) -> str:
    # Cleans up router name from the ESX path for display
    b = os.path.basename(esx_path)
    # Example: "TMO-G5AR February 2026 Survey (No APs).esx" -> "TMO-G5AR"
    s = str(b)
    s = re.sub(r"\s*February\s*\d{4}\s*Survey\s*\(No APs\)\.esx$", "", s, flags=re.IGNORECASE)
    s = re.sub(r"\s*Survey\s*\(No APs\)\.esx$", "", s, flags=re.IGNORECASE)
    s = re.sub(r"\.esx$", "", s, flags=re.IGNORECASE) # Remove .esx if still present
    s = s.strip(" -_")
    return s if s else b # Fallback to base filename if cleanup results in empty string


